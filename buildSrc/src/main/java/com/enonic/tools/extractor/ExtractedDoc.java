package com.enonic.tools.extractor;

public final class ExtractedDoc
{
    private String html;

    private String text;

    public String getHtml()
    {
        return html;
    }

    public void setHtml( final String html )
    {
        this.html = html;
    }

    public String getText()
    {
        return text;
    }

    public void setText( final String text )
    {
        this.text = text;
    }
}
